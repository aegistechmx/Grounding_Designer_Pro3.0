/**
 * Exporta a formato CSV (datos de malla)
 */
export const exportToCSV = (params, calculations) => {
  const { nx, ny } = calculations;
  const spacingX = params.gridLength / Math.max(1, nx - 1);
  const spacingY = params.gridWidth / Math.max(1, ny - 1);
  
  let csvContent = 'X (m),Y (m),Type\n';
  
  // Puntos de la malla
  for (let i = 0; i < nx; i++) {
    for (let j = 0; j < ny; j++) {
      const x = i * spacingX;
      const y = j * spacingY;
      csvContent += `${x.toFixed(3)},${y.toFixed(3)},Node\n`;
    }
  }
  
  const blob = new Blob(['\uFEFF' + csvContent], { type: 'text/csv;charset=utf-8;' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  
  try {
    a.href = url;
    a.download = `grounding_grid_${(params.projectName || 'design').replace(/[^a-z0-9]/gi, '_')}.csv`;
    document.body.appendChild(a);
    a.click();
    console.log('CSV exportado correctamente');
    return true;
  } catch (error) {
    console.error('Error exportando a CSV:', error);
    return false;
  } finally {
    setTimeout(() => {
      if (document.body.contains(a)) document.body.removeChild(a);
      URL.revokeObjectURL(url);
    }, 100);
  }
};

/**
 * Exporta a todos los formatos disponibles
 */
export const exportToAllFormats = (params, calculations) => {
  const results = {
    cdegs: false,
    etap: false,
    skm: false,
    pscad: false,
    csv: false
  };
  
  try {
    results.cdegs = exportToCDEGS(params, calculations);
  } catch (error) {
    console.error('Error exporting to CDEGS:', error);
  }
  
  try {
    results.etap = exportToETAP(params, calculations);
  } catch (error) {
    console.error('Error exporting to ETAP:', error);
  }
  
  try {
    results.skm = exportToSKM(params, calculations);
  } catch (error) {
    console.error('Error exporting to SKM:', error);
  }
  
  try {
    results.pscad = exportToPSCAD(params, calculations);
  } catch (error) {
    console.error('Error exporting to PSCAD:', error);
  }
  
  try {
    results.csv = exportToCSV(params, calculations);
  } catch (error) {
    console.error('Error exporting to CSV:', error);
  }
  
  const successCount = Object.values(results).filter(v => v === true).length;
  alert(`Exportacion completada:\n- CDEGS: ${results.cdegs ? 'OK' : 'FAIL'}\n- ETAP: ${results.etap ? 'OK' : 'FAIL'}\n- SKM: ${results.skm ? 'OK' : 'FAIL'}\n- PSCAD: ${results.pscad ? 'OK' : 'FAIL'}\n- CSV: ${results.csv ? 'OK' : 'FAIL'}\n\n${successCount}/5 formatos exportados`);
  
  return results;
};

export default {
  exportToCDEGS,
  exportToETAP,
  exportToSKM,
  exportToPSCAD,
  exportToCSV,
  exportToAllFormats
};