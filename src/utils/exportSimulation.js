/**
 * Exportación a Software de Simulación
 * Formatos soportados: CDEGS, ETAP, SKM, PSCAD, DigSILENT
 */

/**
 * Valida parámetros antes de exportar
 */
const validateParams = (params, calculations) => {
  const errors = [];
  if (!params) errors.push('Parametros no definidos');
  if (!calculations) errors.push('Calculos no definidos');
  if (!params.soilResistivity) errors.push('Resistividad del suelo no definida');
  if (!params.gridLength || !params.gridWidth) errors.push('Dimensiones de malla no definidas');
  if (!calculations.Rg) errors.push('Resultados de calculos no disponibles');
  
  return {
    isValid: errors.length === 0,
    errors
  };
};

/**
 * Exporta a formato CDEGS (.fot)
 */
export const exportToCDEGS = (params, calculations) => {
  const validation = validateParams(params, calculations);
  if (!validation.isValid) {
    console.error('Error de validacion:', validation.errors);
    alert(`Error al exportar a CDEGS:\n${validation.errors.join('\n')}`);
    return false;
  }

  const cdegsContent = `* CDEGS Input File for Grounding Grid Analysis
* Generated by Grounding Designer Pro - IEEE 80
* Date: ${new Date().toISOString()}
*
* PROJECT INFORMATION
* Project: ${params.projectName || 'Grounding Design'}
* Engineer: ${params.engineerName || 'N/A'}
* Location: ${params.projectLocation || 'N/A'}
*
* SOIL MODEL
* Soil Model: ${params.soilModel || 'Homogeneous'}
* Resistivity: ${params.soilResistivity} ohm-m
* Surface Layer Resistivity: ${params.surfaceLayer} ohm-m
* Surface Layer Thickness: ${params.surfaceDepth} m
* Reflection Factor: ${params.reflectionFactor || 'N/A'}
*
* GRID GEOMETRY
* Grid Dimensions: ${params.gridLength} m x ${params.gridWidth} m
* Grid Area: ${(params.gridLength * params.gridWidth).toFixed(2)} m2
* Grid Depth: ${params.gridDepth} m
* Number of Conductors X: ${params.numParallel}
* Number of Conductors Y: ${params.numParallel}
* Conductor Diameter: ${params.conductorDiameter || 0.01052} m
* Conductor Material: COPPER
* Total Conductor Length: ${calculations.totalConductor?.toFixed(2) || 0} m
*
* RODS
* Number of Rods: ${params.numRods}
* Rod Length: ${params.rodLength} m
* Rod Diameter: ${params.rodDiameter || 0.015875} m
* Total Rod Length: ${(params.numRods * params.rodLength).toFixed(2)} m
*
* FAULT DATA
* Fault Current: ${calculations.faultCurrent?.toFixed(0) || 0} A
* Division Factor Sf: ${params.currentDivisionFactor}
* Grid Current (Ig): ${calculations.Ig?.toFixed(0) || 0} A
* Fault Duration: ${params.faultDuration} s
*
* RESULTS
* Grid Resistance (Rg): ${calculations.Rg?.toFixed(3) || 0} ohm
* Ground Potential Rise (GPR): ${calculations.GPR?.toFixed(0) || 0} V
* Maximum Touch Voltage (Em): ${calculations.Em?.toFixed(0) || 0} V
* Maximum Step Voltage (Es): ${calculations.Es?.toFixed(0) || 0} V
* Allowable Touch Voltage (70kg): ${calculations.Etouch70?.toFixed(0) || 0} V
* Allowable Step Voltage (70kg): ${calculations.Estep70?.toFixed(0) || 0} V
* Safety Margin Touch: ${calculations.Etouch70 && calculations.Etouch70 > 0 ? ((calculations.Etouch70 - calculations.Em) / calculations.Etouch70 * 100).toFixed(1) : '0'}%
* Safety Margin Step: ${calculations.Estep70 && calculations.Estep70 > 0 ? ((calculations.Estep70 - calculations.Es) / calculations.Estep70 * 100).toFixed(1) : '0'}%
* Compliance Status: ${calculations.complies ? 'PASS - Design complies with IEEE 80' : 'FAIL - Design does NOT comply with IEEE 80'}
*
* END OF FILE`;

  const blob = new Blob([cdegsContent], { type: 'text/plain' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  
  try {
    a.href = url;
    a.download = `grounding_${(params.projectName || 'design').replace(/[^a-z0-9]/gi, '_')}_cdegs.fot`;
    document.body.appendChild(a);
    a.click();
    console.log('CDEGS exportado correctamente');
    return true;
  } catch (error) {
    console.error('Error exportando a CDEGS:', error);
    return false;
  } finally {
    setTimeout(() => {
      if (document.body.contains(a)) document.body.removeChild(a);
      URL.revokeObjectURL(url);
    }, 100);
  }
};

